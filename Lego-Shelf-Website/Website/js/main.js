function getLegoCoords(lego){
    if(!document.getElementById("table")){
        let searchSection = document.getElementById('searchSection');
        searchSection.parentNode.removeChild(searchSection);
        createTable()
    }
    
    let table1 = document.getElementById("table");
    let table2 = document.getElementById("table2");
    let coords = lego.coords.split(";");

    if(lego.table == "table1"){
        coords.forEach(coord =>{
            let coords = coord.split(":");
            let x = coords[0];
            let y = coords[1];
            let td = table1.rows[parseInt(x)].cells[parseInt(y)];
            td.setAttribute("bgcolor", "red");
        });
        createLegoInformations(lego);
    }else if(lego.table == "table2"){
        coords.forEach(coord =>{
            let coords = coord.split(":");
            let x = coords[0];
            let y = coords[1];
            let td = table2.rows[parseInt(x)].cells[parseInt(y)];
            td.setAttribute("bgcolor", "red");
        });
        createLegoInformations(lego);
    }
    
    let resultSection = document.getElementById("resultSection");
    let button = document.createElement("button");
    button.innerText = "Reset";
    button.onclick = Reset;
    resultSection.appendChild(button);
}

