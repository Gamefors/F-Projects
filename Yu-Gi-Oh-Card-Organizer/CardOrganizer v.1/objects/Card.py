class Card:
    def __init__(self, name, card, rank, cardType, attribute, speedDuel, atkPoints, defPoints, quantity):
        self.name = name
        self.card = card
        self.rank = rank
        self.cardType = cardType
        self.attribute = attribute
        self.speedDuel = speedDuel
        self.atkPoints = atkPoints
        self.defPoints = defPoints
        self.quantity = quantity