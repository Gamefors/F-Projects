class MonsterCard:
    def __init__(self, cardObject, name, attribute, level, monsterType, cardType, atkPoints, defPoints, text, speedDuel, quantity):
        self.cardObject = cardObject
        self.name = name
        self.attribute = attribute
        self.level = level
        self.monsterType = monsterType
        self.cardType = cardType
        self.atkPoints = atkPoints
        self.defPoints = defPoints
        self.text = text
        self.speedDuel = speedDuel
        self.quantity = quantity